using System;
using System.Text;

namespace Panel.Helpers;

/// <summary>
/// Mã hóa chuỗi nhạy cảm bằng XOR để strings/IDA không thấy plaintext.
/// Key XOR 16 bytes, chuỗi đã encrypt lưu dưới dạng byte array.
/// Khi chạy runtime mới giải mã → tránh static analysis.
/// </summary>
public static class StringShield
{
    // Key XOR 16 bytes ngẫu nhiên
    private static readonly byte[] _xorKey = {
        0x4A, 0xF7, 0x2B, 0x9E, 0xD1, 0x63, 0x8C, 0x05,
        0xB8, 0x1F, 0xE4, 0x72, 0xA6, 0x3D, 0x50, 0xC9
    };

    private static string Decode(byte[] encrypted)
    {
        byte[] result = new byte[encrypted.Length];
        for (int i = 0; i < encrypted.Length; i++)
        {
            result[i] = (byte)(encrypted[i] ^ _xorKey[i % _xorKey.Length]);
        }
        return Encoding.UTF8.GetString(result);
    }

    // "https://zefoxtools.io.vn/licenseapi"
    private static readonly byte[] _licenseApiUrl = {
        0x22, 0x83, 0x5F, 0xEE, 0xA2, 0x59, 0xA3, 0x2A,
        0xC2, 0x7A, 0x82, 0x1D, 0xDE, 0x49, 0x3F, 0xA6,
        0x26, 0x84, 0x05, 0xF7, 0xBE, 0x4D, 0xFA, 0x6B,
        0x97, 0x73, 0x8D, 0x11, 0xC3, 0x53, 0x23, 0xAC,
        0x2B, 0x87, 0x42
    };

    // "ZFox_DPAPI_Secret_Salt_!@#"
    private static readonly byte[] _dpapiSalt = {
        0x10, 0xB1, 0x44, 0xE6, 0x8E, 0x27, 0xDC, 0x44,
        0xE8, 0x56, 0xBB, 0x21, 0xC3, 0x5E, 0x22, 0xAC,
        0x3E, 0xA8, 0x78, 0xFF, 0xBD, 0x17, 0xD3, 0x24,
        0xF8, 0x3C
    };

    // "ZFoxPanel_Unique_App_Mutex"
    private static readonly byte[] _mutexName = {
        0x10, 0xB1, 0x44, 0xE6, 0x81, 0x02, 0xE2, 0x60,
        0xD4, 0x40, 0xB1, 0x1C, 0xCF, 0x4C, 0x25, 0xAC,
        0x15, 0xB6, 0x5B, 0xEE, 0x8E, 0x2E, 0xF9, 0x71,
        0xDD, 0x67
    };

    // "ZFox-"
    private static readonly byte[] _hwidPrefix = {
        0x10, 0xB1, 0x44, 0xE6, 0xFC
    };

    // "https://zefoxtools.io.vn/api/update?channel=stable"
    private static readonly byte[] _updateUrl = {
        0x22, 0x83, 0x5F, 0xEE, 0xA2, 0x59, 0xA3, 0x2A,
        0xC2, 0x7A, 0x82, 0x1D, 0xDE, 0x49, 0x3F, 0xA6,
        0x26, 0x84, 0x05, 0xF7, 0xBE, 0x4D, 0xFA, 0x6B,
        0x97, 0x7E, 0x94, 0x1B, 0x89, 0x48, 0x20, 0xAD,
        0x2B, 0x83, 0x4E, 0xA1, 0xB2, 0x0B, 0xED, 0x6B,
        0xD6, 0x7A, 0x88, 0x4F, 0xD5, 0x49, 0x31, 0xAB,
        0x26, 0x92
    };

    // "license.dat"
    private static readonly byte[] _licenseFile = {
        0x26, 0x9E, 0x48, 0xFB, 0xBF, 0x10, 0xE9, 0x2B,
        0xDC, 0x7E, 0x90
    };

    /// <summary>URL API xác thực license</summary>
    public static string GetLicenseApiUrl() => Decode(_licenseApiUrl);

    /// <summary>Salt cho DPAPI encryption</summary>
    public static string GetDpapiSalt() => Decode(_dpapiSalt);

    /// <summary>Tên Mutex ngăn mở nhiều instance</summary>
    public static string GetMutexName() => Decode(_mutexName);

    /// <summary>Tiền tố HWID</summary>
    public static string GetHwidPrefix() => Decode(_hwidPrefix);

    /// <summary>URL kiểm tra cập nhật</summary>
    public static string GetUpdateUrl() => Decode(_updateUrl);

    /// <summary>Tên file lưu token</summary>
    public static string GetLicenseFileName() => Decode(_licenseFile);
}
